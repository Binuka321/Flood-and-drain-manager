// Current rainfall data (last 30 days)
export const getCurrentRainfallData = () => {
  const districts = Object.keys(rainfallHistoricalData);
  const currentData = {};
  
  for (let i = 0; i < 30; i++) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    const dateStr = date.toISOString().split('T')[0];
    
    currentData[dateStr] = {};
    districts.forEach(district => {
      // Simulate realistic daily rainfall variation
      const avgMonthly = rainfallHistoricalData[district].monthlyData[date.getMonth()].avgRainfall;
      const dailyAvg = avgMonthly / 30;
      const variation = (Math.random() - 0.5) * dailyAvg * 2;
      const rainfall = Math.max(0, dailyAvg + variation);
      
      currentData[dateStr][district] = {
        rainfall: Math.round(rainfall * 10) / 10,
        unit: "mm"
      };
    });
  }
  
  return currentData;
};
